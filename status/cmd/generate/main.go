package main

import (
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
	"text/template"

	"github.com/odigos-io/odigos/status"
)

const reasonEnumTemplate = `// Code generated by "make -C status generate"; DO NOT EDIT.

package {{ .Package }}

const {{ .StatusName }} = "{{ .StatusType }}"

// +kubebuilder:validation:Enum={{ .EnumValues }}
type {{ .TypeName }} string

const (
{{- range .Reasons }}
	// {{ .Message }}
	{{ .ConstName }} {{ $.TypeName }} = "{{ .ReasonName }}"
{{- end }}
)
`

type reasonEnumData struct {
	Package    string
	StatusName string
	StatusType string
	TypeName   string
	EnumValues string
	Reasons    []reasonEnumEntry
}

type reasonEnumEntry struct {
	ConstName  string
	ReasonName string
	Message    string
}

func main() {
	statusDir := flag.String("status-dir", ".", "path to the status module root")
	flag.Parse()

	manifestsDir := filepath.Join(*statusDir, "instrumentationconfig")
	written, err := generateReasonEnums(*statusDir, manifestsDir)
	if err != nil {
		fmt.Fprintf(os.Stderr, "generate: %v\n", err)
		os.Exit(1)
	}

	for _, path := range written {
		fmt.Printf("generated %s\n", path)
	}
}

func generateReasonEnums(outputDir, manifestsDir string) ([]string, error) {
	manifests, err := status.LoadManifestsFromDir(manifestsDir)
	if err != nil {
		return nil, err
	}

	var written []string
	tmpl := template.Must(template.New("reasonEnum").Parse(reasonEnumTemplate))
	for _, manifest := range manifests {
		if manifest.Spec.Codegen == nil {
			continue
		}

		outputPath, data, err := reasonEnumFromManifest(manifest)
		if err != nil {
			return nil, err
		}

		fullPath := filepath.Join(outputDir, outputPath)
		if err := writeReasonEnumFile(tmpl, fullPath, data); err != nil {
			return nil, fmt.Errorf("write %s: %w", fullPath, err)
		}
		written = append(written, fullPath)
	}

	return written, nil
}

func reasonEnumFromManifest(manifest status.Status) (outputPath string, data reasonEnumData, err error) {
	cg := manifest.Spec.Codegen
	if cg.StatusName == "" {
		return "", reasonEnumData{}, fmt.Errorf("manifest %q: codegen.statusName is required", manifest.Metadata.Name)
	}
	if manifest.Spec.Type == "" {
		return "", reasonEnumData{}, fmt.Errorf("manifest %q: spec.type is required", manifest.Metadata.Name)
	}
	if cg.TypeName == "" {
		return "", reasonEnumData{}, fmt.Errorf("manifest %q: codegen.typeName is required", manifest.Metadata.Name)
	}
	if cg.Package == "" {
		return "", reasonEnumData{}, fmt.Errorf("manifest %q: codegen.package is required", manifest.Metadata.Name)
	}
	if cg.OutputFile == "" {
		return "", reasonEnumData{}, fmt.Errorf("manifest %q: codegen.outputFile is required", manifest.Metadata.Name)
	}

	constPrefix := cg.ConstPrefix
	if constPrefix == "" {
		constPrefix = cg.TypeName
	}

	outputPath = cg.OutputFile
	if !strings.Contains(outputPath, "/") {
		outputPath = filepath.Join(cg.Package, outputPath)
	}

	data = reasonEnumData{
		Package:    cg.Package,
		StatusName: cg.StatusName,
		StatusType: manifest.Spec.Type,
		TypeName:   cg.TypeName,
	}
	for _, reason := range manifest.Spec.Reasons {
		data.Reasons = append(data.Reasons, reasonEnumEntry{
			ConstName:  constPrefix + reason.Name,
			ReasonName: reason.Name,
			Message:    commentSafeMessage(reason.Message),
		})
	}
	data.EnumValues = reasonEnumValues(data.Reasons)

	return outputPath, data, nil
}

func reasonEnumValues(reasons []reasonEnumEntry) string {
	names := make([]string, len(reasons))
	for i, reason := range reasons {
		names[i] = reason.ReasonName
	}
	return strings.Join(names, ";")
}

func commentSafeMessage(message string) string {
	message = strings.TrimSpace(message)
	if message == "" {
		return ""
	}
	return strings.Join(strings.Fields(strings.ReplaceAll(message, "\n", " ")), " ")
}

func writeReasonEnumFile(tmpl *template.Template, path string, data reasonEnumData) error {
	var b strings.Builder
	if err := tmpl.Execute(&b, data); err != nil {
		return err
	}

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return fmt.Errorf("format generated source: %w", err)
	}

	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, formatted, 0o644)
}
